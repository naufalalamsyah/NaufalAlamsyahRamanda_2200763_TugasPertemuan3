package soal3;
public class Soal3 {
    public static void main(String[] args) {
        // Mendapatkan kode ASCII dari karakter '4' dan '6'
        int asciiValue4 = (int) '4';
        int asciiValue6 = (int) '6';
        
        // Menjumlahkan kedua nilai ASCII
        int sum = asciiValue4 + asciiValue6;
        
        // Menampilkan hasil penjumlahan
        System.out.println("Hasil penjumlahan ASCII dari '4' dan '6' adalah: " + sum);
    }
}


